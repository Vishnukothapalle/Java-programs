                        /*program for Alphabets */
class Alphabets
{
    public static void main(String args[])
    {
        char s;
        System.out.println("The alphabets are");
        for(s=97;s<=122;s++)
        {
            System.out.print(" "+s);
        }
    }
}
/*output:
The alphabets are
 a b c d e f g h i j k l m n o p q r s t u v w x y z

*/